// Primitive Types:-

// Number
    var num = 100;
    console.log(typeof num);

// String
   let str1 = 'Hello JS!';
   let str2 = '132'
   console.log(typeof str1);
   console.log(typeof str2);


// Boolean
   let flag = false;
   console.log(typeof flag);

// Null
   let empty = null
   console.log(typeof empty);

// Undefined
    let a;
    console.log(typeof a);


// Reference types or Non-primitive data type or Object type

// Object
let parson = {
    firstName: 'Nuhail',
    lastName: 'Fayez',
    age: 19
}
console.log(typeof parson)

// Array
let arr = [1,2,3,4];
console.log(typeof arr)

